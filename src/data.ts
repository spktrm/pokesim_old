function parseCSV(path: string) {
    let obj: Object;
    parse(
        fs.readFileSync(path, { encoding: "utf-8" }),
        {
            delimiter: ",",
        },
        (error, result) => {
            if (error) {
                console.error(error);
            }
            console.log("Result", result);
        }
    );
    return obj;
}

